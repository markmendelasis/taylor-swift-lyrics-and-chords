// ADDEventlistener
var backgroundColor = document.getElementById("main")
// Taylor First Album
var selfTitledAlbum = document.getElementById("selfTitled");

selfTitledAlbum.addEventListener("mouseenter", function(){
    selfTitledAlbum.style.transform = "scale(1.5)";
    selfTitledAlbum.style.marginBottom = ("2vh");
    backgroundColor.style.background = `url("https://i.insider.com/5716327f9105844e018bd968?width=948&format=jpeg")`;
    backgroundColor.style.backgroundSize = "500px";
    
    
}); 
selfTitledAlbum.addEventListener("mouseleave", function(){
    selfTitledAlbum.style.transform = "scale(1)";
    selfTitledAlbum.style.marginBottom = ("0");
    backgroundColor.style.background = `unset`;
    
});

